function processCommand() {
    var isValid = false;

    // Create args list by splitting the command
    // by space characters and then shift off the
    // actual command.
    
    command = strip(command); //try to remove bad things for people trying to break my site. Cough Cough CrackleHarvest36, I know you and your <script> tag shenanigans

    var args = command.split(" ");
    var cmd = args[0];
    args.shift();

    // Iterate through the available commands to find a match.
    // Then call that command and pass in any arguments.
    for (var i = 0; i < commands.length; i++) {
        if (cmd === commands[i].name) {
            printToHistory("website@ericsserver ~ $ " + command);
            commands[i].function(args);
            isValid = true;
            break;
        }
    }

    // No match was found...
    if (!isValid) {
        printToHistory("bash: lshw: command not found: '" + command + "'");
    }

    // Add to command history and clean up.
    commandHistory.push(command);
    historyIndex = commandHistory.length;
    command = "";
    prompt.html("website@ericsserver ~ $ ");
}

$(document).keydown(function (e) {
    e = e || window.event;
    var keyCode = typeof e.which === "number" ? e.which : e.keyCode;

    // BACKSPACE
    if (keyCode === 8 && e.target.tagName !== "INPUT" && e.target.tagName !== "TEXTAREA") {
        e.preventDefault();
        if (command !== "") {
            erase(1);
        }
    }

    // UP or DOWN
    if (keyCode === 38 || keyCode === 40) {
        // Move up or down the history
        if (keyCode === 38) {
            // UP
            historyIndex--;
            if (historyIndex < 0) {
                historyIndex++;
            }
        } else if (keyCode === 40) {
            // DOWN
            historyIndex++;
            if (historyIndex > commandHistory.length - 1) {
                historyIndex--;
            }
        }

        // Get command
        var cmd = commandHistory[historyIndex];
        if (cmd !== undefined) {
            clearCommand();
            appendCommand(cmd);
        }
    }
});

$(document).keypress(function (e) {
    // Make sure we get the right event
    e = e || window.event;
    var keyCode = typeof e.which === "number" ? e.which : e.keyCode;

    // Which key was pressed?
    switch (keyCode) {
        // ENTER
        case 13: {
            terminal.append("\n");

            processCommand();
            break;
        }
        default: {
            appendCommand(String.fromCharCode(keyCode));
        }
    }
});
